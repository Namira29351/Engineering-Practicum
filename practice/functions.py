def multiply(num1, num2):
    num3 = num1 * num2 
    return num3 

print("testing multiply function: ", multiply(num1 = 77, num2 = 999))

# divison
def divide(num1, num2):
    num3 = num1 / num2
    return num3

print("testing divide funtion: ", divide(num1 = 42, num2 = 6))


# squares
def exponents(num1, num2):
    num3 = num1 ** num2
    return num3

print("testing square function: ", exponents(num1 = 3, num2 = 2))

# square root 
import math
def root(num1):
    num2 = math.sqrt(num1)
    return num2

print("testing root function: ", root(num1 = 9))